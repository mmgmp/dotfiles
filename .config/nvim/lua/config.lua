-- Número de la linea
vim.opt.number = true

-- Números relativos de lineas
vim.opt.relativenumber = true

-- Mostrar linea del cursor
vim.opt.cursorline = false

-- Colores de la terminal
vim.opt.termguicolors = false

-- Número de espacios de tabulador
vim.opt.tabstop = 4

-- Número de espacios automáticos
vim.opt.shiftwidth = 4

-- Número de espacios cuando se usa <BS> o <Del>
vim.opt.softtabstop = 4

-- Activar el ratón
vim.opt.mouse = "a"

-- Portapapeles del sistema
vim.opt.clipboard = 'unnamedplus'

-- Mostrar columna de la izquierda
vim.opt.signcolumn = 'yes'

-- Movimiento mas rápido
vim.opt.ttyfast = true

-- Mostrar siempre 10 lineas por debajo del cursor
vim.opt.scrolloff = 10

-- Buscar los mayúsculas
vim.o.ignorecase = true

-- Solo mayúsculas si se especifica
vim.o.smartcase = true
